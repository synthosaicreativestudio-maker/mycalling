/**
 * "Адвокат дьявола" (Д-4): анти-социально-желательная техника.
 * Возвращает блок с инструкцией по мягкому оспариванию шаблонного ответа —
 * но ТОЛЬКО один раз на шаг (guard хранится в extractedData._devilsAdvocateUsed).
 * Мутирует extractedData (тот же объект, что и в вызывающем коде chat/route.ts),
 * чтобы отметка "уже применялось" сохранилась в БД вместе с остальным extractedData
 * даже если ИИ проигнорирует мягкую инструкцию "не дважды подряд".
 */
function getDevilsAdvocateBlock(extractedData: any, stepId: number): string {
  if (!extractedData) return '';
  if (!Array.isArray(extractedData._devilsAdvocateUsed)) {
    extractedData._devilsAdvocateUsed = [];
  }
  if (extractedData._devilsAdvocateUsed.includes(stepId)) {
    return '';
  }
  extractedData._devilsAdvocateUsed.push(stepId);
  return `

ПРОВЕРКА НА ШАБЛОННОСТЬ: если ответ подростка выглядит социально желаемым или шаблонным («хочу в IT, это модно», «люблю учиться», ответ без личной детали) — ОДИН раз мягко оспорь его парадоксальным вопросом (например: «А если через 5 лет код будут писать алгоритмы сами — тогда зачем учиться программировать?»). Не дави. Если после оспаривания подросток подтверждает выбор с новой личной аргументацией — прими её и зафиксируй. Никогда не оспаривай дважды подряд на одном шаге.`;
}

