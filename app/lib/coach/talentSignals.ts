/**
 * Детерминированная страховка «Колеса талантов» (docs/27 Трек 4).
 *
 * Если ИИ-экстрактор не выставил баллы, но в реплике подростка есть явный сигнал
 * сферы, соответствующая шкала обязана загореться. Здесь — чистая, тестируемая
 * детекция сигналов по ключевым словам. Применение (Math.max с полом 45) — в route.
 */

export type TalentScale =
  | 'creative' | 'tech' | 'science' | 'data' | 'social' | 'organizational' | 'startup';

const SIGNALS: Record<TalentScale, string[]> = {
  creative: ['дизайн', 'рисова', 'рисую', 'музык', 'текст', 'видео', 'фото', 'мода', 'творч', 'арт', 'анимац'],
  tech: ['программ', ' it ', 'it-', 'айти', 'ит-компан', 'разработ', ' код', 'сайт', 'приложен', 'робот', 'гейм', 'инженер', 'компьютер', 'нейросет'],
  science: ['физик', 'хими', 'биолог', 'астроном', 'эксперимент', 'наук', 'исследова', 'космос', 'генетик'],
  data: ['математик', 'статистик', 'данны', 'аналитик', 'метрик', 'формул', 'расчёт', 'расчет', 'финанс', 'логик'],
  social: ['общени', 'помога', 'обуча', 'преподава', 'продаж', 'психолог', 'медицин', 'блог', 'волонт', 'люди', 'врач'],
  organizational: ['менеджмент', 'организ', 'процесс', 'планир', 'администр', 'логистик', 'порядок'],
  startup: ['бизнес', 'стартап', 'предприним', 'компани', 'лидер', 'влияни', 'масштаб', 'слава', 'деньги', 'амбиц', 'основа'],
};

/** Минимальный балл, который выставляется шкале при явном сигнале. */
export const SIGNAL_FLOOR = 45;

/**
 * Возвращает список шкал, для которых в сообщении найден явный сигнал.
 * Регистронезависимо; сопоставление по подстрокам-корням.
 */
export function detectTalentSignals(message: string): TalentScale[] {
  const lower = ` ${(message || '').toLowerCase()} `;
  const matched: TalentScale[] = [];
  for (const scale of Object.keys(SIGNALS) as TalentScale[]) {
    if (SIGNALS[scale].some(w => lower.includes(w))) matched.push(scale);
  }
  return matched;
}
