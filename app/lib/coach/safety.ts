  'Пожалуйста, поделись тем, что чувствуешь, с взрослым, которому доверяешь, или позвони — это бесплатно и анонимно:\n\n' +
  '• Детский телефон доверия: 8-800-2000-122 (круглосуточно, по всей России)\n' +
  '• Экстренная помощь: 112\n\n' +
  'Ты можешь вернуться к нашему разговору о будущем в любой момент, когда будешь готов. Береги себя.';
