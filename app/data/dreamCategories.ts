/**
 * Грубый словарный маппинг ключевых слов мечты/цели подростка на буквы RIASEC.
 * Используется только индексом согласованности (app/lib/profile/consistency.ts)
 * как дешёвый первый слой — не претендует на полноту.
 */
export const dreamKeywordsToRiasec: Record<string, string[]> = {
  R: ['механик', 'инженер', 'строител', 'ремонт', 'техник', 'мастер', 'спорт', 'военн', 'пилот', 'повар'],
  I: ['учён', 'наука', 'исследовател', 'аналитик', 'программист', 'разработчик', 'врач', 'математик', 'данны'],
  A: ['художник', 'дизайнер', 'музыкант', 'актёр', 'актер', 'писател', 'блогер', 'творчеств', 'режиссёр', 'режиссер'],
  S: ['учител', 'психолог', 'врач', 'волонтёр', 'волонтер', 'социальн', 'помога', 'коуч', 'воспитател'],
  E: ['предпринимател', 'бизнес', 'директор', 'лидер', 'управля', 'стартап', 'продаж', 'маркетолог'],
  C: ['бухгалтер', 'юрист', 'администратор', 'аналитик данных', 'логист', 'делопроизвод'],
};

export function detectDreamRiasecCodes(text: string): string[] {
  const lower = text.toLowerCase();
  const matched: string[] = [];
  Object.entries(dreamKeywordsToRiasec).forEach(([code, keywords]) => {
    if (keywords.some((kw) => lower.includes(kw))) matched.push(code);
  });
  return matched;
}
