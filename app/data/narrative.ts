/**
 * Достаёт главу по testCode/test_type в любом регистре.
 * Возвращает null, если блок неизвестен narrative-словарю (например, новый
 * тип вопроса ещё не описан — тогда UI просто не показывает переходный экран).
 */
export function getNarrative(testCode: string | null | undefined): NarrativeChapter | null {
  if (!testCode) return null;
  const key = testCode.toUpperCase() as NarrativeTestCode;
  return narrative[key] ?? null;
}

